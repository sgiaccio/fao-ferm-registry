                                                1.6 Context
                                            </h3>
                                            <div class="my-3 ml-7">
                                                <span class="text-sm"
                                                    >The Tongluo Mountain is one
                                                    of the "four mountains" in
                                                    Chongqing and the "lungs" of
                                                    the urban area. It is an
                                                    important ecological
                                                    conservation area and
                                                    barrier, playing an
                                                    important role in the
                                                    ecological security of the
                                                    Yangtze River. To meet the
                                                    needs of early
                                                    socio-economic development,
                                                    ore mining in this area was
                                                    extremely frequent. It was
                                                    an important source of
                                                    building materials for a
                                                    certain period, leading to
                                                    the ecosystem's
                                                    degradation.<br />With the
                                                    increasing awareness of
                                                    ecological conservation, the
                                                    local government implemented
                                                    a comprehensive mine closure
                                                    plan in this area during
                                                    2010 and 2012. After that,
                                                    41 abandoned mines were
                                                    left, with an affected area
                                                    of 14.87 km2. Ecological
                                                    restoration was urgently
                                                    needed to address huge
                                                    safety hazards and severe
                                                    ecological problems such as
                                                    low land use efficiency,
                                                    poor ecosystem quality and
                                                    stability, collapsed
                                                    biodiversity, and reduced
                                                    water conservation
                                                    capacity.<br />Based on the
                                                    above background, the local
                                                    government has actively
                                                    promoted the implementation
                                                    of the integrated
                                                    restoration and conservation
                                                    project in this area since
                                                    2015.</span
