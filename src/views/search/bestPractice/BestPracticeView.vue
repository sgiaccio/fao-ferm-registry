                                                2.1 Description
                                            </h3>
                                            <div class="my-3 ml-7">
                                                <span class="text-sm"
                                                    >Adhering to the concept
                                                    that "mountains, rivers,
                                                    forests, farmland, lakes,
                                                    and grasslands are a
                                                    community of life", the
                                                    systematic restoration of
                                                    abandoned mine land in
                                                    Tongluo mountain had been
                                                    carried out. Suitable
                                                    restoration measures were
                                                    implemented in different
                                                    areas according to a unified
                                                    plan. <br />After the mine
                                                    site restoration,
                                                    eco-tourism, like the
                                                    display of mining relics,
                                                    was developed based on the
                                                    location advantages and the
                                                    surrounding industrial
                                                    layout, which promoted the
                                                    transformation of mine
                                                    practitioners and residents
                                                    into ecological tour
                                                    practitioners. Thus,
                                                    ecological restoration was
                                                    connected with rural
                                                    revitalization and the
                                                    improvement of people's
                                                    livelihoods. <br />By the
                                                    end of 2022, the project had
                                                    completed a cumulative
                                                    investment of 291.65 million
                                                    RMB, of which 158.3 million
                                                    RMB, 126.87 million RMB, and
                                                    6.48 million RMB were
                                                    invested by the central
                                                    government, local
                                                    government, and the market,
                                                    respectively. In total,
                                                    150.57 ha (accounting for
                                                    52.57%) of abandoned mine
                                                    land was restored. Based on
                                                    the restoration, more than
                                                    140 ha of new arable land,
                                                    forest land, and other
                                                    agricultural land were
                                                    built; 68 side slopes with
                                                    potential safety hazards
                                                    were eliminated; 333 ha
                                                    (accounting for 14.41%) of
                                                    land was remediated.</span
                                                >
