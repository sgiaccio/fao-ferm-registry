                                            </h3>
                                            <div class="my-3 ml-7">
                                                <span class="text-sm"
                                                    >1. Introduction of invasive
                                                    species<br />Mining had
                                                    caused the loss of original
                                                    vegetation, resulting in a
                                                    sharp decline in
                                                    biodiversity. After that,
                                                    more than 30 invasive
                                                    plants, including Erigeron
                                                    annuus, Alternanthera
                                                    philoxeroides,
                                                    Symphyotrichum subulatum,
                                                    Trifolium repens, Phytolacca
                                                    americana, Ipomoea purpurea
                                                    and Bidens Pilosa, were
                                                    recorded in farmlands and
                                                    shrub lands. In some areas,
                                                    invasive plants had become
                                                    the dominant species,
                                                    occupying the habitats of
                                                    native plants and crops,
                                                    resulting in a decline in
                                                    the quality and value of
                                                    agricultural and forestry
                                                    products.<br />Countermeasure
                                                    1. Delineating the scope of
                                                    conservation, restoring
                                                    natural habitat, and
                                                    conserving valuable species
                                                    in mountain areas to enhance
                                                    biodiversity.
                                                    <br />Countermeasure 2.
                                                    Planting species with
                                                    ecological or economic
                                                    values along roads in mine
                                                    sites to replace invasive
                                                    alien species. Multi-layered
                                                    vegetation structures were
                                                    created or optimized by
                                                    coniferous and broad-leaved
                                                    mixed forests and
                                                    multi-layer mixed
                                                    forests.<br />Countermeasure
                                                    3. Uprooting invasive alien
                                                    species manually to ensure
                                                    the effectiveness of
                                                    ecological restoration.<br />2.
                                                    Extractive industry
                                                    development<br />The Tongluo
                                                    Mountain is rich in
                                                    limestone resources, and
                                                    mining activities were
                                                    extremely frequent. In the
                                                    1990s, there were hundreds
                                                    of mining enterprises.
                                                    Large-scale open-pit mining
                                                    activities caused landform
                                                    destruction and ecological
                                                    degradation.<br />Countermeasures
                                                    (since 2013): Carrying out
                                                    ecological restoration in
                                                    abandoned mine land based on
                                                    eco-environment surveys and
                                                    unified planning to reduce
                                                    soil erosion, eliminate
                                                    geological hazards, increase
                                                    carbon sequestration
                                                    capacity, and gradually lead
                                                    the ecosystem into natural
                                                    succession.</span
                                                >
