                    <h2 class="font-bold">
                        What is a good practice for ecosystem restoration?
                    </h2>
                    <br />
                    <div>
                        A good practice for ecosystem restoration is an
                        <span class="font-bold"
                            >evidence-based approach, technique or
                            technology</span
                        >
                        that contributes to achieving one or more objectives of
                        a restoration initiative, maximizing benefits for nature
                        and people across different contexts.
                        <span class="font-bold"
                            >It is usually a component of a restoration
                            initiative that has been applied, tested and
                            replicated in different contexts and therefore, can
                            be easily transferred and/or adapted to other
                            initiatives with similar goals.</span
                        >If a practice has been tested solely in a specific
                        context, it is considered a promising practice, then
                        results need to be proven outside the current situation
                        for replicability and adaptability to different
                        contexts.
